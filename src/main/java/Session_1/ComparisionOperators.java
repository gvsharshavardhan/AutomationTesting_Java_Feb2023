package Session_1;

public class ComparisionOperators {
    public static void main(String[] args) {

        int a = 10;
        int b = 10;
        int c = 20;

        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>=b);

        System.out.println(!(a==b));

        System.out.println(10>20 && 34<89);

        String fname = "busy";
        String lname = "qa";
        double abc = 100;

        System.out.println(fname + lname + abc);
    }
}
